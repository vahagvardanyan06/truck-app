class CardItem {
  final String title;
  final String imageUrl;
  final String location;
  final String speed;
  final bool isLocked;
  final String time;

  CardItem({
    required this.title,
    required this.imageUrl,
    required this.location,
    required this.speed,
    required this.isLocked,
    required this.time,
  });
}
